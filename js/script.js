/** SCRIPT DE BARRA DE PROGRESO **/

function move() {
	var elem = document.getElementById("myBar");
	var width = 0;
	var id = setInterval(frame, 1);
	function frame() {
		if (width >= 100) {
			clearInterval(id);
		} else {
			width++;
			elem.style.width = width + '%';
		}
	}
}



